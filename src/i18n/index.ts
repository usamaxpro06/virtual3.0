// Custom language detector with IP-based detection
const customLanguageDetector = {
  name: 'customDetector',
  lookup: async () => {
    try {
      // Try to get user's country from IP
      const response = await fetch('https://ipapi.co/json/');
      const data = await response.json();
      const country = data.country_code?.toLowerCase();
      
      // Map countries to languages
      const countryToLanguage: { [key: string]: string } = {
        'sa': 'ar', // Saudi Arabia
        'ae': 'ar', // UAE
        'eg': 'ar', // Egypt
        'ma': 'ar', // Morocco
        'tn': 'ar', // Tunisia
        'dz': 'ar', // Algeria
        'jo': 'ar', // Jordan
        'lb': 'ar', // Lebanon
        'sy': 'ar', // Syria
        'iq': 'ar', // Iraq
        'kw': 'ar', // Kuwait
        'qa': 'ar', // Qatar
        'bh': 'ar', // Bahrain
        'om': 'ar', // Oman
        'ye': 'ar', // Yemen
        'fr': 'fr', // France
        'be': 'fr', // Belgium (has French speakers)
        'ch': 'fr', // Switzerland (has French speakers)
        'ca': 'fr', // Canada (has French speakers)
        'sn': 'fr', // Senegal
        'ci': 'fr', // Ivory Coast
        'ml': 'fr', // Mali
        'bf': 'fr', // Burkina Faso
        'ne': 'fr', // Niger
        'td': 'fr', // Chad
        'mg': 'fr', // Madagascar
      };
      
      return countryToLanguage[country] || 'en';
    } catch (error) {
      console.log('IP detection failed, using browser language');
      return navigator.language.split('-')[0] || 'en';
    }
  },
  cacheUserLanguage: () => {
    // Don't cache since we want to detect on each visit
  }
};

i18n
  .use({
    type: 'languageDetector',
    async: true,
    init: () => {},
    detect: customLanguageDetector.lookup,
    cacheUserLanguage: customLanguageDetector.cacheUserLanguage
  })
  .use(initReactI18next)
  .init({
    resources,
    fallbackLng: 'en',
    debug: false,
    interpolation: {
      escapeValue: false
    },
    detection: {
      order: ['customDetector', 'navigator', 'htmlTag'],
      caches: []
    }
  });

export default i18n;